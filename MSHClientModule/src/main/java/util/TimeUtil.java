public class TimeUtil {

    public int year;
    public int month;
    public int day;

    public int hour;
    public int min;
    public int sec;

    public TimeUtil(int year, int month, int day, int hour, int min, int sec) {
        this.year = year;
        this.month = month;
        this.day = day;
        this.hour = hour;
        this.min = min;
        this.sec = sec;
    }

}
